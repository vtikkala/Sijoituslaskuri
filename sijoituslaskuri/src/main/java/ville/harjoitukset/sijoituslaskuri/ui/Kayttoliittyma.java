    private void lataaSijoituskohteet() {
        try {
           lukija = new Scanner(new File("sijoituskohteet.txt"));
           
           while (lukija.hasNextLine()) {
               String luettuRivi = lukija.nextLine();
               rahastot.add(luoSijoituskohde(luettuRivi));
           }
           
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }
    
    private Rahasto luoSijoituskohde(String rivi) {
        String tunnus = rivi.substring(0, 18);
        tunnus = tunnus.replaceAll("\\s+", "");
        
        String nimi = rivi.substring(19, 38);
        int luokka = Integer.parseInt(rivi.substring(39, 40));
        double tuotto = Double.parseDouble(rivi.substring(40, 45));
        int tyyppi = Integer.parseInt(rivi.substring(46, 47));
        double merkintapalkkio = Double.parseDouble(rivi.substring(47, 52));
        double lunastuspalkkio = Double.parseDouble(rivi.substring(53, 58));
        double hallinnointipalkkio = Double.parseDouble(rivi.substring(59, 64));
        
        Rahasto rahasto = new Rahasto(tunnus, nimi, luokka, tuotto, 0.00, tyyppi, merkintapalkkio, 
                lunastuspalkkio, hallinnointipalkkio);
        
        return rahasto;
    }
    
    
    
