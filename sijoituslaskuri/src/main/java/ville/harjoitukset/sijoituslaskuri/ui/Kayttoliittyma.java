    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        double sijoitus = Double.parseDouble(jTextField4.getText());
        int sijoitusaika = Integer.parseInt(jTextField5.getText());
        Suunnitelma suunnitelma = new Suunnitelma(sijoitusaika, 1, sijoitus);
        
        Simulaattori simulaattori = new Simulaattori(this.portfolio, suunnitelma);
        simulaattori.simuloi();
        
        double dSijoitusYhteensa = simulaattori.getSijoitus();
        String sijoitusYhteensa = String.format("%.2f", dSijoitusYhteensa);
        jLabel20.setText(sijoitusYhteensa);
        
        double dTuototYhteensa = simulaattori.getTuotto();
        String tuototYhteensa = String.format("%.2f", dTuototYhteensa);
        jLabel28.setText(tuototYhteensa);
        
        double dKulutYhteensa = -1 * simulaattori.getKulut();
        String kulutYhteensa = String.format("%.2f", dKulutYhteensa);
        jLabel17.setText(kulutYhteensa);
        
        double dSijoitusLopussa = simulaattori.getSijoitusLopussa();
        String sijoitusLopussa = String.format("%.2f", dSijoitusLopussa);
        jLabel13.setText(sijoitusLopussa);
    }//GEN-LAST:event_jButton6ActionPerformed

